package ga.forntoh.bableschool.model;

@SuppressWarnings("unused")
public class Category {

    private int id;
    private String title, thumbnail, color;

    public String getTitle() {
        return title;
    }

    public String getThumbnail() {
        return thumbnail;
    }

    public String getColor() {
        return color;
    }

    public int getId() {
        return id;
    }
}
