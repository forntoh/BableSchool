package ga.forntoh.bableschool.model;

public class Category {

    private int id;
    private String title;
    private String thumbnail;
    private String color;

    public Category() {
    }

    public String getTitle() {
        return title;
    }

    public String getThumbnail() {
        return thumbnail;
    }

    public String getColor() {
        return color;
    }

    public int getId() {
        return id;
    }
}
